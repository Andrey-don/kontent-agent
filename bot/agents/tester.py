from bot.utils.openrouter import call_agent

MODEL = "anthropic/claude-haiku-4-5"
TEMPERATURE = 0.1

SYSTEM_PROMPT = """Ты — тестировщик контента. Проверяешь пост по строгому чек-листу.

Чек-лист:
□ Длина 150–300 слов
□ Первые 1–2 предложения — крючок (результат или провокация)
□ Короткие предложения, разговорный стиль
□ Пост про клиента, не про инструмент
□ Провокационный вопрос перед CTA
□ Кодовое слово в CTA (заглавными буквами)
□ 3–5 эмодзи

Формат ответа:
ВЕРДИКТ: ✅ Одобрено / ❌ На доработку

Если ❌ — укажи конкретно что не так и что исправить."""


def run(post: str) -> str:
    context = f"""
ПОСТ ДЛЯ ПРОВЕРКИ:
{post}

Проверь по чек-листу. Вынеси вердикт.
"""
    return call_agent(SYSTEM_PROMPT, context, MODEL, TEMPERATURE)
